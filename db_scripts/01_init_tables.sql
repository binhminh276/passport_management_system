CREATE OR REPLACE PROCEDURE PASSPORT_APP.PROC_TAO_HO_SO (
    p_cccd IN VARCHAR2,
    p_ho_ten IN VARCHAR2,
    p_gioi_tinh IN VARCHAR2,
    p_ngay_sinh IN DATE,
    p_sdt IN VARCHAR2,
    p_email IN VARCHAR2,
    p_noi_dung_de_nghi IN VARCHAR2,
    p_co_quan_tiep_nhan IN VARCHAR2,
    p_anh_chan_dung IN VARCHAR2
) AS
    v_reg_id NUMBER;
BEGIN
    INSERT INTO PASSPORT_APP.PASSPORT_DATA (
        CCCD, HO_TEN, GIOI_TINH, NGAY_SINH, SDT, EMAIL, 
        NOI_DUNG_DE_NGHI, CO_QUAN_TIEP_NHAN, ANH_CHAN_DUNG_PATH, TRANG_THAI
    ) VALUES (
        p_cccd, p_ho_ten, p_gioi_tinh, p_ngay_sinh, p_sdt, p_email,
        p_noi_dung_de_nghi, p_co_quan_tiep_nhan, p_anh_chan_dung, 'Da nop'
    ) RETURNING REG_ID INTO v_reg_id;

    INSERT INTO PASSPORT_APP.PASSPORT_REQUEST_HISTORY (
        REG_ID, TRANG_THAI_CU, TRANG_THAI_MOI, NGUOI_CAP_NHAT
    ) VALUES (
        v_reg_id, NULL, 'Da nop', 'He thong'
    );
    COMMIT;
END;
