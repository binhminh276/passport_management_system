ALTER SESSION SET CONTAINER = FREEPDB1;

CREATE USER PASSPORT_APP IDENTIFIED BY 123;
GRANT CONNECT, RESOURCE, UNLIMITED TABLESPACE TO PASSPORT_APP;

CREATE USER SEC_MGR IDENTIFIED BY 123;
GRANT CONNECT, RESOURCE, UNLIMITED TABLESPACE TO SEC_MGR;
GRANT EXECUTE ON DBMS_RLS TO SEC_MGR; 
GRANT AUDIT SYSTEM TO SEC_MGR; 
GRANT CREATE ANY CONTEXT TO SEC_MGR; 
GRANT EXEMPT ACCESS POLICY TO SEC_MGR;
GRANT ADMINISTER ROW LEVEL SECURITY POLICY TO SEC_MGR;

CREATE ROLE ROLE_XT;
CREATE ROLE ROLE_XD;
CREATE ROLE ROLE_LT;
CREATE ROLE ROLE_GS;
CREATE ROLE ROLE_CD;

CREATE TABLE PASSPORT_APP.TINH_THANH (
    MA_TINH VARCHAR2(10) PRIMARY KEY,
    TEN_TINH VARCHAR2(100) NOT NULL
);

CREATE TABLE PASSPORT_APP.PHUONG_XA (
    MA_PHUONG VARCHAR2(10) PRIMARY KEY,
    TEN_PHUONG VARCHAR2(100) NOT NULL,
    MA_TINH VARCHAR2(10) NOT NULL,
    CONSTRAINT FK_PHUONG_TINH FOREIGN KEY (MA_TINH) REFERENCES PASSPORT_APP.TINH_THANH(MA_TINH)
);

CREATE TABLE PASSPORT_APP.DAN_TOC (
    MA_DAN_TOC VARCHAR2(10) PRIMARY KEY,
    TEN_DAN_TOC VARCHAR2(50) NOT NULL
);

CREATE TABLE PASSPORT_APP.TON_GIAO (
    MA_TON_GIAO VARCHAR2(10) PRIMARY KEY,
    TEN_TON_GIAO VARCHAR2(100) NOT NULL
);

CREATE TABLE PASSPORT_APP.RESIDENT_DATA (
    CCCD VARCHAR2(12) PRIMARY KEY,
    HO_TEN VARCHAR2(100) NOT NULL,
    NGAY_SINH DATE NOT NULL,
    GIOI_TINH VARCHAR2(10),
    DIA_CHI_THUONG_TRU VARCHAR2(255)
);

CREATE TABLE PASSPORT_APP.PASSPORT_DATA (
    REG_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CCCD VARCHAR2(12) NOT NULL,
    HO_TEN VARCHAR2(100) NOT NULL,
    GIOI_TINH VARCHAR2(10) NOT NULL,
    NGAY_SINH DATE NOT NULL,
    NOI_SINH VARCHAR2(100),
    MA_DAN_TOC VARCHAR2(10),
    MA_TON_GIAO VARCHAR2(10),
    SDT VARCHAR2(15) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL,
    MA_TINH_THUONG_TRU VARCHAR2(10),
    MA_PHUONG_THUONG_TRU VARCHAR2(10),
    DIA_CHI_CHI_TIET_TT VARCHAR2(255),
    MA_TINH_TAM_TRU VARCHAR2(10),
    MA_PHUONG_TAM_TRU VARCHAR2(10),
    DIA_CHI_CHI_TIET_TAM_TRU VARCHAR2(255),
    NGHE_NGHIEP VARCHAR2(100),
    TEN_DC_CO_QUAN VARCHAR2(255),
    HO_TEN_CHA VARCHAR2(100),
    NS_CHA DATE,
    HO_TEN_ME VARCHAR2(100),
    NS_ME DATE,
    HO_TEN_VO_CHONG VARCHAR2(100),
    NS_VO_CHONG DATE,
    HC_GAN_NHAT VARCHAR2(50),
    NGAY_CAP_HC DATE,
    NOI_CAP_HC VARCHAR2(100),
    NOI_DUNG_DE_NGHI VARCHAR2(255) NOT NULL,
    CO_QUAN_TIEP_NHAN VARCHAR2(255) NOT NULL,
    HINH_THUC_NHAN VARCHAR2(50),
    STK_HOAN_TIEN VARCHAR2(50),
    TEN_CHU_TK VARCHAR2(100),
    NGAN_HANG VARCHAR2(100),
    ANH_CHAN_DUNG_PATH VARCHAR2(255),
    HO_SO_DINH_KEM_PATH CLOB,
    TRANG_THAI VARCHAR2(50) DEFAULT 'Da nop',
    THOI_GIAN_TAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_PASSPORT_DANTOC FOREIGN KEY (MA_DAN_TOC) REFERENCES PASSPORT_APP.DAN_TOC(MA_DAN_TOC),
    CONSTRAINT FK_PASSPORT_TONGIAO FOREIGN KEY (MA_TON_GIAO) REFERENCES PASSPORT_APP.TON_GIAO(MA_TON_GIAO),
    CONSTRAINT FK_PASSPORT_TINH_TT FOREIGN KEY (MA_TINH_THUONG_TRU) REFERENCES PASSPORT_APP.TINH_THANH(MA_TINH),
    CONSTRAINT FK_PASSPORT_PHUONG_TT FOREIGN KEY (MA_PHUONG_THUONG_TRU) REFERENCES PASSPORT_APP.PHUONG_XA(MA_PHUONG),
    CONSTRAINT FK_PASSPORT_TINH_TAM FOREIGN KEY (MA_TINH_TAM_TRU) REFERENCES PASSPORT_APP.TINH_THANH(MA_TINH),
    CONSTRAINT FK_PASSPORT_PHUONG_TAM FOREIGN KEY (MA_PHUONG_TAM_TRU) REFERENCES PASSPORT_APP.PHUONG_XA(MA_PHUONG)
);

CREATE TABLE PASSPORT_APP.REGULATIONS (
    QUY_DINH_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOI_DUNG VARCHAR2(500) NOT NULL
);

CREATE TABLE PASSPORT_APP.PASSPORT_REQUEST_HISTORY (
    HISTORY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    REG_ID NUMBER NOT NULL,
    TRANG_THAI_CU VARCHAR2(50),
    TRANG_THAI_MOI VARCHAR2(50) NOT NULL,
    THOI_GIAN_CAP_NHAT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    NGUOI_CAP_NHAT VARCHAR2(50)
);

CREATE TABLE PASSPORT_APP.APP_USERS (
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(50) UNIQUE NOT NULL,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    DB_ROLE VARCHAR2(50) NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1,
    LAST_LOGIN TIMESTAMP
);

ALTER TABLE PASSPORT_APP.PASSPORT_DATA
ADD CONSTRAINT FK_PASSPORT_RESIDENT 
FOREIGN KEY (CCCD) REFERENCES PASSPORT_APP.RESIDENT_DATA(CCCD);

GRANT CREATE USER TO SEC_MGR;
GRANT GRANT ANY PRIVILEGE TO SEC_MGR;
GRANT GRANT ANY ROLE TO SEC_MGR;
GRANT INSERT, SELECT ON PASSPORT_APP.APP_USERS TO SEC_MGR;
GRANT EXECUTE ON SA_USER_ADMIN TO SEC_MGR;

CREATE USER APP_GUEST IDENTIFIED BY 123;
GRANT CONNECT TO APP_GUEST;

GRANT SELECT, UPDATE ON PASSPORT_APP.PASSPORT_DATA TO SEC_MGR;
GRANT SELECT, UPDATE ON PASSPORT_APP.RESIDENT_DATA TO SEC_MGR;

GRANT SELECT, INSERT, UPDATE ON PASSPORT_APP.PASSPORT_DATA TO ROLE_XT, ROLE_XD, ROLE_LT, ROLE_GS, ROLE_CD;
GRANT SELECT, UPDATE ON PASSPORT_APP.RESIDENT_DATA TO ROLE_XT, ROLE_XD, ROLE_GS;
GRANT SELECT ON PASSPORT_APP.DAN_TOC TO ROLE_XT, ROLE_XD, ROLE_LT, ROLE_GS, ROLE_CD;
GRANT SELECT ON PASSPORT_APP.TON_GIAO TO ROLE_XT, ROLE_XD, ROLE_LT, ROLE_GS, ROLE_CD;
GRANT SELECT ON PASSPORT_APP.TINH_THANH TO ROLE_XT, ROLE_XD, ROLE_LT, ROLE_GS, ROLE_CD;
GRANT SELECT ON PASSPORT_APP.PHUONG_XA TO ROLE_XT, ROLE_XD, ROLE_LT, ROLE_GS, ROLE_CD;
GRANT SELECT ON PASSPORT_APP.REGULATIONS TO ROLE_XT, ROLE_XD, ROLE_LT, ROLE_GS, ROLE_CD;

GRANT SELECT ON PASSPORT_APP.PASSPORT_REQUEST_HISTORY TO ROLE_XT, ROLE_XD, ROLE_LT, ROLE_GS, ROLE_CD;
GRANT INSERT ON PASSPORT_APP.PASSPORT_REQUEST_HISTORY TO ROLE_XT, ROLE_XD, ROLE_LT, ROLE_CD;
